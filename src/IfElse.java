public class IfElse {
    public static void main(String[] args) {
        int x = 0;
        if(x>0){
            System.out.println("x is greater than 0");
        } else if(x == 0){
            System.out.println("x is less than 0");
        }else{
            System.out.println("x is equal to 0");
        }
    }
}
